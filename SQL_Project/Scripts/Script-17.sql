SELECT
	gc.ID,
	gc.BILLCODE,
	gc.JIAZ 
FROM
	GAMS_CARD gc
LEFT JOIN GAMS_JC_ASSETSORT_GB gjag ON
	gc.SORTGB = gjag.ID
LEFT JOIN GAMS_JC_ENTRYFORM gje ON
	gc.RUZXS = gje.ID
LEFT JOIN BIZ_JY00_GAMS_JC_JIAOYUSAGE bjgjj ON
	gc.JIAOYSYFX = BJGJJ .ID
WHERE
	gjag.NAME = '03'
	AND gc.ISLASTEST = 1
	AND gc.ORGUNIT = '6E35BC1F80000001F6D62BD49F8EC9CC'
	AND gje.NAME = '01'
	AND gc.CARDSTATE LIKE '0%'
	AND gc.SHIXRQ > TO_DATE('2020-12-31 00:00:00', 'yyyy-mm-dd HH24:mi:ss')
	AND gc.id NOT IN (
	SELECT
		id
	FROM
		GAMS_CARD_ZYSB gct
	WHERE
		gct.ISLASTEST = 1
		AND gct.ORGUNIT = '6E35BC1F80000001F6D62BD49F8EC9CC');
		
	SELECT *FROM GAMS_CARD_CHANGE gcc WHERE gcc.SORTGB = '3711F12FC000000192E940E7FAE32E55' AND gcc.ORG = '6E35BC1F80000001F6D62BD49F8EC9CC';